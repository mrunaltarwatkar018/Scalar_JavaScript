// ES6 Every Features:




// 1. **Let and Const**: Introduced two new ways to declare variables, let and const. Let
// is used to declare a variable that can be reassigned, whereas const is used to
// declare a constant variable that cannot be reassigned.
// 2. **Arrow Functions**: Introduced a new way to declare functions using the arrow
// notation. Arrow functions are concise and do not have their own this context.
// 3. **Template Literals**: Introduced a new way to create strings using template
// literals. Template literals allow you to embed expressions inside string literals.
// 4. **Default Parameters**: Introduced a new way to set default values for function
// parameters. Default parameters allow you to specify a default value for a
// parameter if it is not provided.
// 5. **Rest Parameters**: Introduced a new way to handle variable number of
// arguments in functions using rest parameters. Rest parameters allow you to
// capture the rest of the arguments in an array.